const prompt = require('prompt-sync')();
let num = Number(prompt("Enter a number : "));

if (num == 1 )
        console.log ("SUNDAY")
else if (num == 2 )
        console.log ("MONDAY")
else if (num == 3 )
        console.log ("THUSDAY")
else if (num == 4 )
        console.log ("WEDNESDAY")
else if (num == 5 )
        console.log ("THURSDAY")
else if (num == 6 )
        console.log ("FRIDAY")
else if (num == 7 )
        console.log ("SATURDAY")
else
        console.log ("invalid")